package scalax.scalawt

/** A simple widget displaying text and/or an icon.
  *
  * Icons are optional, because, depending on the view, displaying images may
  * not be supported (TUIs), i.e. one should always include a textual
  * representation.
  */
class Label(text: String/*, icon: Option[Icon]*/) extends Component {
  override def toString = "Label(%s)" format text
}
